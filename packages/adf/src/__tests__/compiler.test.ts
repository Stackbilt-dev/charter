describe('compileAdf — banner', () => {
  it('claude target starts with html comment banner', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toMatch(/^<!--/);
    expect(result.output).toContain(COMPILE_BANNER_MARKER);
  });

  it('agents target starts with html comment banner', () => {
    const result = compileAdf(makeOptions('agents'));
    expect(result.output).toMatch(/^<!--/);
    expect(result.output).toContain(COMPILE_BANNER_MARKER);
  });

  it('cursor target starts with hash comment banner', () => {
    const result = compileAdf(makeOptions('cursor'));
    expect(result.output).toMatch(/^# GENERATED by/);
    expect(result.output).not.toMatch(/^<!--/);
    expect(result.output).toContain(COMPILE_BANNER_MARKER);
  });

  it('gemini target starts with html comment banner', () => {
    const result = compileAdf(makeOptions('gemini'));
    expect(result.output).toMatch(/^<!--/);
    expect(result.output).toContain(COMPILE_BANNER_MARKER);
  });
});

