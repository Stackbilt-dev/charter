describe('compileAdf — determinism', () => {
  it('produces byte-identical output on repeated calls with same input', () => {
    const opts = makeOptions('claude');
    const first = compileAdf(opts).output;
    const second = compileAdf(opts).output;
    expect(first).toBe(second);
  });

  it('produces byte-identical output across all targets for same content', () => {
    // Content should differ only in banner style and title
    const claude = compileAdf(makeOptions('claude')).output;
    const agents = compileAdf(makeOptions('agents')).output;
    // Body content after the banner+title should be the same
    const extractBody = (out: string): string =>
      out.split('\n').slice(4).join('\n'); // skip banner + blank + title + blank
    expect(extractBody(claude)).toBe(extractBody(agents));
  });

  it('output ends with exactly one newline', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toMatch(/\n$/);
    expect(result.output).not.toMatch(/\n\n$/);
  });
});

