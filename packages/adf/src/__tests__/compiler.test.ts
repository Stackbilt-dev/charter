describe('compileAdf — manifest-only sections excluded', () => {
  it('does not include DEFAULT_LOAD section in output', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).not.toContain('## DEFAULT_LOAD');
  });

  it('does not include ON_DEMAND section in output', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).not.toContain('## ON_DEMAND');
  });

  it('does not include DEFAULT_LOAD routing plumbing in output', () => {
    const result = compileAdf(makeOptions('claude'));
    // Verify manifest routing plumbing is skipped (these are in manifest.adf, not content modules)
    expect(result.output).not.toContain('## DEFAULT_LOAD');
    expect(result.output).not.toContain('## ON_DEMAND');
    expect(result.output).not.toContain('## BUDGET');
  });

  it('does not inline on-demand module bodies', () => {
    const result = compileAdf({
      target: 'claude',
      aiDir: '/ai',
      readFile: makeReadFile({
        ...FILES,
        '/ai/frontend.adf': 'ADF: 0.1\nCONSTRAINTS:\n  - Use React hooks\n',
      }),
    });
    // The on-demand module body should NOT appear as compiled content
    expect(result.output).not.toContain('Use React hooks');
  });
});

