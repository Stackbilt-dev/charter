import { describe, it, expect } from 'vitest';
import {
  compileAdf,
  buildBanner,
  COMPILE_BANNER_MARKER,
  COMPILE_TARGETS,
  MODULE_INDEX_FENCE,
  TARGET_FILENAMES,
} from '../compiler';
import type { CompileOptions } from '../compiler';

// ============================================================================
// Fixtures
// ============================================================================

const MANIFEST_ADF = `ADF: 0.1
🎯 ROLE: Test project context router

📦 DEFAULT_LOAD:
  - core.adf
  - state.adf

📂 ON_DEMAND:
  - frontend.adf (Triggers on: React, CSS, UI)
  - backend.adf (Triggers on: API, Node, DB)

📐 RULES:
  - Prefer smallest relevant module set.
  - Never assume unseen modules were loaded.
`;

const CORE_ADF = `ADF: 0.1

📋 CONTEXT:
  - TypeScript monorepo
  - pnpm workspaces

⚠️ CONSTRAINTS [load-bearing]:
  - No secrets in commits
  - Use conventional commits

📊 METRICS [load-bearing]:
  entry_loc: 142 / 200 [lines]
`;

const STATE_ADF = `ADF: 0.1

🧠 STATE:
  CURRENT: Active development
  NEXT: Ship v1.0
`;

const FILES: Record<string, string> = {
  '/ai/manifest.adf': MANIFEST_ADF,
  '/ai/core.adf': CORE_ADF,
  '/ai/state.adf': STATE_ADF,
};

function makeReadFile(files: Record<string, string>): (p: string) => string {
  return (p: string): string => {
    const content = files[p];
    if (content === undefined) throw new Error(`File not found: ${p}`);
    return content;
  };
}

function makeOptions(target: CompileOptions['target'], extra?: Record<string, string>): CompileOptions {
  return {
    target,
    aiDir: '/ai',
    readFile: makeReadFile({ ...FILES, ...extra }),
  };
}

