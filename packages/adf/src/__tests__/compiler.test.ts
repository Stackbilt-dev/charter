describe('compileAdf — default module content', () => {
  it('renders CONTEXT section from core.adf', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toContain('## CONTEXT');
    expect(result.output).toContain('TypeScript monorepo');
  });

  it('renders CONSTRAINTS section with weight annotation', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toContain('## CONSTRAINTS _(load-bearing)_');
    expect(result.output).toContain('No secrets in commits');
  });

  it('renders METRICS section from core.adf', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toContain('## METRICS _(load-bearing)_');
    expect(result.output).toContain('`entry_loc`: 142 / 200 [lines]');
  });

  it('renders STATE section from state.adf', () => {
    const result = compileAdf(makeOptions('claude'));
    expect(result.output).toContain('## STATE');
    expect(result.output).toContain('Active development');
  });

  it('modules are rendered in declaration order (core before state)', () => {
    const result = compileAdf(makeOptions('claude'));
    const contextPos = result.output.indexOf('## CONTEXT');
    const statePos = result.output.indexOf('## STATE');
    expect(contextPos).toBeLessThan(statePos);
  });
});

