describe('adf compile — JSON stdout mode', () => {
  it('emits JSON with target, output, modules, nextActions', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const logs = await captureLog(() =>
      adfCommand(JSON_OPTIONS, ['compile', '--target', 'claude']),
    );

    const result = JSON.parse(logs[0]);
    expect(result.target).toBe('claude');
    expect(result.output).toContain(COMPILE_BANNER_MARKER);
    expect(result.defaultModules).toEqual(['core.adf', 'state.adf']);
    expect(result.onDemandModules).toHaveLength(1);
    expect(result.onDemandModules[0].path).toBe('frontend.adf');
    expect(result.nextActions).toBeDefined();
    expect(Array.isArray(result.nextActions)).toBe(true);
  });
});

// ============================================================================
// --write mode
// ============================================================================

describe('adf compile — --write mode', () => {
  it('writes CLAUDE.md to the working directory', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);

    expect(exitCode).toBe(0);
    const written = fs.readFileSync(path.join(tmp, 'CLAUDE.md'), 'utf-8');
    expect(written).toContain(COMPILE_BANNER_MARKER);
    expect(written).toContain('## CONTEXT');
  });

  it('writes .cursorrules with hash banner for cursor target', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'cursor', '--write']);

    const written = fs.readFileSync(path.join(tmp, '.cursorrules'), 'utf-8');
    expect(written).toMatch(/^# GENERATED by/);
  });

  it('--target all writes all four files', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const exitCode = await adfCommand(TEXT_OPTIONS, [
      'compile', '--target', 'all', '--write',
    ]);

    expect(exitCode).toBe(0);
    expect(fs.existsSync(path.join(tmp, 'CLAUDE.md'))).toBe(true);
    expect(fs.existsSync(path.join(tmp, 'AGENTS.md'))).toBe(true);
    expect(fs.existsSync(path.join(tmp, '.cursorrules'))).toBe(true);
    expect(fs.existsSync(path.join(tmp, 'GEMINI.md'))).toBe(true);
  });

  it('refuses to overwrite a file without the compile banner (hand-written)', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // Hand-written file — no compile banner
    fs.writeFileSync(path.join(tmp, 'CLAUDE.md'), '# My hand-written CLAUDE.md\n\nDo not overwrite me.\n');

    const spy = vi.spyOn(console, 'error').mockImplementation(() => {});
    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);
    spy.mockRestore();

    // Should be refused — exit 1 (POLICY_VIOLATION)
    expect(exitCode).toBe(1);
    // Original file should be unchanged
    const content = fs.readFileSync(path.join(tmp, 'CLAUDE.md'), 'utf-8');
    expect(content).toBe('# My hand-written CLAUDE.md\n\nDo not overwrite me.\n');
  });

  it('refuses to overwrite pointer stubs without --force', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // Pointer stub from `adf init --emit-pointers` — no compile banner
    const pointerStub = '# CLAUDE.md\n\n> **DO NOT add rules, constraints, or context to this file.**\n';
    fs.writeFileSync(path.join(tmp, 'CLAUDE.md'), pointerStub);

    const spy = vi.spyOn(console, 'error').mockImplementation(() => {});
    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);
    spy.mockRestore();

    expect(exitCode).toBe(1);
    const content = fs.readFileSync(path.join(tmp, 'CLAUDE.md'), 'utf-8');
    expect(content).toBe(pointerStub);
  });

  it('--force overwrites a file without the compile banner', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    fs.writeFileSync(path.join(tmp, 'CLAUDE.md'), '# Old hand-written content\n');

    const exitCode = await adfCommand(TEXT_OPTIONS, [
      'compile', '--target', 'claude', '--write', '--force',
    ]);

    expect(exitCode).toBe(0);
    const content = fs.readFileSync(path.join(tmp, 'CLAUDE.md'), 'utf-8');
    expect(content).toContain(COMPILE_BANNER_MARKER);
  });

  it('overwrites an existing compile artifact without --force', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // First write
    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);
    // Second write — should succeed without --force since banner is present
    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);

    expect(exitCode).toBe(0);
    const content = fs.readFileSync(path.join(tmp, 'CLAUDE.md'), 'utf-8');
    expect(content).toContain(COMPILE_BANNER_MARKER);
  });

  it('--write JSON output lists written files and nextActions', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const logs = await captureLog(() =>
      adfCommand(JSON_OPTIONS, ['compile', '--target', 'claude', '--write']),
    );

    const result = JSON.parse(logs[0]);
    expect(result.written).toContain('CLAUDE.md');
    expect(result.refused).toEqual([]);
    expect(Array.isArray(result.nextActions)).toBe(true);
  });
});

