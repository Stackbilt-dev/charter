describe('adf compile — --check mode', () => {
  it('exits 0 when compiled output matches file on disk', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // First write the file so it matches
    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);

    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--check']);
    expect(exitCode).toBe(0);
  });

  it('exits 1 when file on disk is stale', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // Write a stale / hand-modified version
    fs.writeFileSync(
      path.join(tmp, 'CLAUDE.md'),
      `<!-- ${COMPILE_BANNER_MARKER} -->\n\n# CLAUDE.md\n\nStale content that differs.\n`,
    );

    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--check']);
    expect(exitCode).toBe(1);
  });

  it('exits 1 when file does not exist on disk', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--check']);
    expect(exitCode).toBe(1);
  });

  it('--target all exits 0 when all files are current', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all', '--write']);
    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all', '--check']);
    expect(exitCode).toBe(0);
  });

  it('--target all exits 1 when any file is stale', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all', '--write']);
    // Corrupt one file
    const agentsMd = path.join(tmp, 'AGENTS.md');
    fs.writeFileSync(agentsMd, fs.readFileSync(agentsMd, 'utf-8') + '\n# Extra stale content\n');

    const exitCode = await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all', '--check']);
    expect(exitCode).toBe(1);
  });

  it('--check JSON output reports stale/current/missing arrays', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    // Write CLAUDE.md but leave AGENTS.md missing
    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write']);

    const logs = await captureLog(() =>
      adfCommand(JSON_OPTIONS, ['compile', '--target', 'all', '--check']),
    );

    const result = JSON.parse(logs[0]);
    expect(result.current).toContain('CLAUDE.md');
    expect(result.missing).toContain('AGENTS.md');
    expect(result.upToDate).toBe(false);
    expect(Array.isArray(result.nextActions)).toBe(true);
  });

  it('--check JSON output sets upToDate true when all current', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all', '--write']);

    const logs = await captureLog(() =>
      adfCommand(JSON_OPTIONS, ['compile', '--target', 'all', '--check']),
    );

    const result = JSON.parse(logs[0]);
    expect(result.upToDate).toBe(true);
    expect(result.stale).toEqual([]);
    expect(result.missing).toEqual([]);
  });
});
