/**
 * CLI-level tests for `charter adf compile`.
 *
 * Tests:
 * - stdout rendering (single target)
 * - --write with overwrite protection (banner check, --force bypass)
 * - --check stale/current paths
 * - error handling (missing manifest, bad --target, mutually exclusive flags)
 * - JSON output format
 *
 * Uses temp-dir fixture .ai/ tree following the pattern of adf-init.test.ts.
 */

import * as fs from 'node:fs';
import * as os from 'node:os';
import * as path from 'node:path';
import { afterEach, describe, expect, it, vi } from 'vitest';
import type { CLIOptions } from '../index';
import { adfCommand } from '../commands/adf';
import { COMPILE_BANNER_MARKER } from '@stackbilt/adf';

// ============================================================================
// Test helpers
// ============================================================================

const TEXT_OPTIONS: CLIOptions = {
  configPath: '.charter',
  format: 'text',
  ciMode: false,
  yes: false,
};

const JSON_OPTIONS: CLIOptions = {
  configPath: '.charter',
  format: 'json',
  ciMode: false,
  yes: false,
};

const originalCwd = process.cwd();
const tempDirs: string[] = [];

afterEach(() => {
  process.chdir(originalCwd);
  while (tempDirs.length > 0) {
    const dir = tempDirs.pop();
    if (dir) fs.rmSync(dir, { recursive: true, force: true });
  }
  vi.restoreAllMocks();
});

function makeTmp(): string {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), 'charter-compile-test-'));
  tempDirs.push(dir);
  process.chdir(dir);
  return dir;
}

/** Set up a minimal .ai/ fixture in the current tmpdir. */
function scaffoldAi(tmp: string, extra?: Record<string, string>): void {
  const aiDir = path.join(tmp, '.ai');
  fs.mkdirSync(aiDir, { recursive: true });

  fs.writeFileSync(path.join(aiDir, 'manifest.adf'), [
    'ADF: 0.1',
    '',
    '🎯 ROLE: Test project',
    '',
    '📦 DEFAULT_LOAD:',
    '  - core.adf',
    '  - state.adf',
    '',
    '📂 ON_DEMAND:',
    '  - frontend.adf (Triggers on: React, CSS)',
    '',
  ].join('\n'));

  fs.writeFileSync(path.join(aiDir, 'core.adf'), [
    'ADF: 0.1',
    '',
    '📋 CONTEXT:',
    '  - TypeScript monorepo',
    '',
    '⚠️ CONSTRAINTS [load-bearing]:',
    '  - No secrets in commits',
    '',
  ].join('\n'));

  fs.writeFileSync(path.join(aiDir, 'state.adf'), [
    'ADF: 0.1',
    '',
    '🧠 STATE:',
    '  CURRENT: Active development',
    '',
  ].join('\n'));

  for (const [filename, content] of Object.entries(extra ?? {})) {
    fs.writeFileSync(path.join(tmp, filename), content);
  }
}

function captureStdout(fn: () => Promise<number> | number): Promise<string> {
  return new Promise((resolve, reject) => {
    const chunks: string[] = [];
    const orig = process.stdout.write.bind(process.stdout);
    process.stdout.write = (chunk: string | Uint8Array): boolean => {
      chunks.push(String(chunk));
      return true;
    };
    Promise.resolve(fn())
      .then(() => {
        process.stdout.write = orig;
        resolve(chunks.join(''));
      })
      .catch(err => {
        process.stdout.write = orig;
        reject(err);
      });
  });
}

function captureLog(fn: () => Promise<number> | number): Promise<string[]> {
  return new Promise((resolve, reject) => {
    const captured: string[] = [];
    const spy = vi.spyOn(console, 'log').mockImplementation((s: string) => captured.push(s));
    Promise.resolve(fn())
      .then(() => {
        spy.mockRestore();
        resolve(captured);
      })
      .catch(err => {
        spy.mockRestore();
        reject(err);
      });
  });
}

// ============================================================================
// Stdout mode (no --write / --check)
// ============================================================================

describe('adf compile — stdout mode', () => {
  it('prints compiled output to stdout for a single target', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const output = await captureStdout(() =>
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude']),
    );

    expect(output).toContain(COMPILE_BANNER_MARKER);
    expect(output).toContain('# CLAUDE.md');
    expect(output).toContain('## CONTEXT');
    expect(output).toContain('TypeScript monorepo');
  });

  it('cursor target uses hash comment banner', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    const output = await captureStdout(() =>
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'cursor']),
    );

    expect(output).toMatch(/^# GENERATED by/);
    expect(output).not.toMatch(/^<!--/);
  });

  it('throws when --target all used without --write or --check', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await expect(
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'all']),
    ).rejects.toThrow('requires --write or --check');
  });

  it('throws on invalid --target', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await expect(
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'copilot']),
    ).rejects.toThrow('Invalid --target');
  });

  it('throws when --target missing', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await expect(
      adfCommand(TEXT_OPTIONS, ['compile']),
    ).rejects.toThrow('--target');
  });

  it('throws when manifest.adf not found', async () => {
    makeTmp(); // no scaffoldAi

    await expect(
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude']),
    ).rejects.toThrow('manifest.adf not found');
  });

  it('throws on --write and --check together', async () => {
    const tmp = makeTmp();
    scaffoldAi(tmp);

    await expect(
      adfCommand(TEXT_OPTIONS, ['compile', '--target', 'claude', '--write', '--check']),
    ).rejects.toThrow('mutually exclusive');
  });
});

